#include <unistd.h>

void	ft_print(int grid[4][4])
{
	int		i;
	int		j;
	char	n;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			n = grid[i][j] + '0';
			write(1, &n, 1);
			if (j < 3)
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		j = 0;
		i++;
	}
}
